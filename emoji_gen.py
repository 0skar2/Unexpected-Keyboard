def to_java_repr(s):
	s = s.decode('utf-8').encode('utf-16be')
	rep = ""
	i = 0
	for c in s:
		if i <= 0:
			i = 2
			rep += "\\u"
		rep += hex(ord(c))[2:].zfill(2).upper()
		i -= 1
	return rep

